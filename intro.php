                        <h1 class="section" style="text-align:center">When Science meets
                            Respect for Life's Harmony</h1>
                        <p>Les partisans de l'écologie profonde estiment que le monde n'est
                            pas une ressource exploitable à volonté par l'Homme. L'éthique de
                            l'écologie profonde explique qu'un système global (la Nature) est
                            supérieur à chacune de ses parties (l'Homme étant une partie de la
                            nature). Cette éthique s'appuie sur les huit postulats suivants:</p>
                        <ol>
                            <li>Le bien-être et l'épanouissement des formes de vie humaines et
                                non-humaines de la Terre ont une valeur en elles-mêmes (synonyme:
                                valeur intrinsèque, valeur inhérente). Ces valeurs sont
                                indépendantes de l'utilité du monde non-humain pour les besoins
                                humains.</li>
                            <li>La richesse et la diversité des formes de vie contribuent à la
                                réalisation de ces valeurs et sont également des valeurs
                                elles-mêmes.</li>
                            <li>L'Homme n'a pas le droit de réduire la richesse et la diversité
                                biologique, sauf pour satisfaire des besoins humains vitaux.</li>
                            <li>L'épanouissement de la vie et des cultures humaines est
                                compatible avec une <a href="https://fr.wikipedia.org/wiki/D%C3%A9croissance_d%C3%A9mographique" target="_blank" title="Décroissance démographique">décroissance</a>
                                substantielle de la population humaine. Le développement des
                                formes de vie non-humaines requiert une telle diminution.</li>
                            <li>L'interférence humaine actuelle avec le monde non-humain est
                                excessive et nuisible, et la situation empire rapidement.</li>
                            <li>Des politiques doivent donc être changées. Ces politiques
                                affectent les structures économiques, technologiques, et
                                idéologiques fondamentales. Il en résultera une société
                                profondément différente de la nôtre.</li>
                            <li>Les changements idéologiques passent par l'appréciation d'une
                                bonne qualité de vie plutôt que l'adhésion à des standards de vie
                                toujours plus élevés. Il faut prendre conscience de la différence
                                entre « bonne qualité » et « course à un niveau de vie extrêmement
                                élevé » (qui serait néfaste à la nature).</li>
                            <li>Ceux qui souscrivent aux points précédents s'engagent à essayer
                                de mettre en application directement ou indirectement les
                                changements nécessaires.</li>
                        </ol>
